#include <stdio.h>
#include <stdlib.h>

float multiplicar(int numeroUno, float numeroDos)
{
	float resultado;
	resultado= numeroUno * numeroDos;

	return resultado;
}

float dividir(int numeroUno, float numeroDos)
{
	float resultado;
	resultado= numeroUno / numeroDos;

	return resultado;
}
int resta(int numeroUno, int numeroDos)
{
	int resultado;
	resultado= numeroUno - numeroDos;

	return resultado;
}
